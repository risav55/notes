def ray():
    a = input("Enter value of x and y coordinates for first point: ")
    P1 = a.split(",")
    x1 = float(P1[0])
    y1 = float(P1[1])

    b = input("Enter value of x and y coordinates for second point: ")
    P2 = b.split(",")
    x2 = float(P2[0])
    y2 = float(P2[1])

    start = [x1, x2]
    end = [y1, y2]

    print("The first point is : ", P1)
    print("The second point is : ", P2)
    

    slope = (y2-y1)/(x2-x1)

    plt.xlabel('x - axis')
    plt.ylabel('y - axis')
    plt.title('Ray')
    plt.grid()
    plt.plot(start, end, marker="o",  markeredgecolor="red", markerfacecolor="green")
    ax = plt.axes()
    ax.arrow(x2, y2, slope, slope, head_width=0.05, head_length=0.05 )
    plt.show()

