def graph(x,y,title):
    plt.xlabel('x - axis')
    plt.ylabel('y - axis')
    plt.title(title)
    plt.grid()
    plt.plot(x, y, marker="o",  markeredgecolor="red", markerfacecolor="green")
    plt.show()


